// Helper to get gallery for a provider
export function getProviderGallery(type: string): string[] {
  return GALLERY_IMAGES[type] || GALLERY_IMAGES.clinic;
}

// Helper to get image for a provider
export function getProviderImage(type: string): string {
  return PROVIDER_IMAGES[type] || PROVIDER_IMAGES.clinic;
}

// Helper to get schedule for a provider type
export function getDefaultSchedule(type: string, isEmergency: boolean = false) {
  if (isEmergency) {
    return {
      lundi: { open: '00:00', close: '23:59' },
      mardi: { open: '00:00', close: '23:59' },
      mercredi: { open: '00:00', close: '23:59' },
      jeudi: { open: '00:00', close: '23:59' },
      vendredi: { open: '00:00', close: '23:59' },
      samedi: { open: '00:00', close: '23:59' },
      dimanche: { open: '00:00', close: '23:59' },
    };
  }

  const config = DEFAULT_SCHEDULES[type] || DEFAULT_SCHEDULES.clinic;
  
  return {
    lundi: { open: config.open, close: config.close },
    mardi: { open: config.open, close: config.close },
    mercredi: { open: config.open, close: config.close },
    jeudi: { open: config.open, close: config.close },
    vendredi: { open: '08:00', close: '12:00' }, // Friday morning only in Algeria
    samedi: config.weekend ? { open: config.open, close: config.close } : { open: '09:00', close: '12:00', closed: false },
    dimanche: config.weekend ? { open: config.open, close: config.close } : { open: '00:00', close: '00:00', closed: true },
  };
}

// Helper to get reviews for a provider
export function getProviderReviews(providerId: string) {
  return TEST_REVIEWS[providerId] || [];
}
