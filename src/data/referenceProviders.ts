// Helper to generate small random offset for realistic positioning
function offsetCoord(base: number, maxOffset: number = 0.008): number {
  return base + (Math.random() - 0.5) * 2 * maxOffset;
}

function randomElement<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

